  clang::SourceLocation addLoc = wclRewriter_.findLocForNext(decl->getLocEnd(), ';');
  clang::SourceRange replaceRange(addLoc, addLoc);
  std::stringstream inits;
  inits << wclRewriter_.getTransformedText(replaceRange) << ";";

